package org.geekhub.taras.object;


import org.geekhub.taras.storage.Entity;

public class Cat extends Entity {

    private String name;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
